.MODEL SMALL
.STACK 100H
.DATA
	T1 DW ?
	x1 DW ?
	T2 DW ?
	T3 DW ?
	a1 DW ?
	b1 DW ?
.CODE
;------printing procedure----
PRINT_ID PROC

	;SAVE IN STACK
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX

	;CHECK IF NEGATIVE
	OR AX, AX
	JGE PRINT_NUMBER

	;PRINT MINUS SIGN
	PUSH AX
	MOV AH, 2
	MOV DL, '-'
	INT 21H
	POP AX

	NEG AX

	PRINT_NUMBER:
	XOR CX, CX
	MOV BX, 10D

	REPEAT_CALC:

		;AX:DX- QUOTIENT:REMAINDER
		XOR DX, DX
		DIV BX  ;DIVIDE BY 10
		PUSH DX ;PUSH THE REMAINDER IN STACK

		INC CX

		OR AX, AX
		JNZ REPEAT_CALC

	MOV AH, 2

	PRINT_LOOP:
		POP DX
		ADD DL, 30H
		INT 21H
		LOOP PRINT_LOOP

	;NEWLINE
	MOV AH, 2
	MOV DL, 0AH
	INT 21H
	MOV DL, 0DH
	INT 21H

	POP DX
	POP CX
	POP BX
	POP AX

	RET
PRINT_ID ENDP

f PROC

	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH a1

	MOV AX, 2
	POP BX
	MUL BX
	MOV T1, AX

	POP DX
	POP CX
	POP BX
	POP AX
RET
f ENDP

g PROC

	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH b1
	PUSH a1
 
	POP AX
	MOV DX, AX
 
	CALL f

	MOV AX, T1
	ADD AX, DX
	MOV T2, AX

	MOV AX, T2
	POP BX
	ADD AX, BX
	MOV T3, AX
 	POP DX
	POP CX
	POP BX
	POP AX
RET
g ENDP

MAIN PROC

	;INITIALIZE DATA SEGMENT
	MOV AX, @DATA
	MOV DS, AX

 
	MOV AX, 1
	MOV a1, AX
 
	MOV AX, 2
	MOV b1, AX
 
	MOV AX, a1

	MOV AX, b1
	CALL g

	MOV AX, T3
	MOV a1, AX
 ;--------print function called---------

	MOV AX, a1
	CALL PRINT_ID
 
	MOV AX, 4CH
	INT 21H
MAIN ENDP

END MAIN